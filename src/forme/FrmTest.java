    private void btnPosaljiTestSamoproceneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPosaljiTestSamoproceneActionPerformed
        int brojDa = 0, brojNe = 0;
        for (JComboBox cb : cbs) {
            Odgovor odg = Odgovor.valueOf((String) cb.getSelectedItem());
            if (odg == Odgovor.izaberite) {
                JOptionPane.showMessageDialog(this, "Molimo vas da odgovorite na sva pitanja");
                return;
            }
            if (odg == Odgovor.da) {
                brojDa++;
            }
        }
        boolean pozitivan = brojDa >= 2;
        Test test = new Test(-1, TipTesta.samoprocena, new Date(), pozitivan, StatusTesta.poslato, frmKorisnik.getPrijavljen());
        try {
            test = Kontroler.getInstance().posaljiTest(test);
        } catch (Exception ex) {
            //Logger.getLogger(FrmTest.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
            return;
        }

        if (test.isPozitivan()) {
            JOptionPane.showMessageDialog(this, "Rezultat testa samoprocene je pozitivan. Potrebno je da uradite brzi test i/ili PCR test");
            pnlTestSamoprocene.setVisible(false);
            btnPocniTestSamoprocene.setVisible(false);
            pnlBrziIPCR.setVisible(true);
            btnVratiSe.setVisible(false);
            return;
        }
        if (frmKorisnik.getPrijavljen().getStanje() == Stanje.pod_nadzorom) {
            JOptionPane.showMessageDialog(this, "Rezultat testa samoprocene je negativan po drugi put, tako da nije potrebno da budete pod daljim nadzorom. ");
        } else {
            JOptionPane.showMessageDialog(this, "Rezultat testa samoprocene je negativan. U narednom periodu ćete biti pod nadzorom..");
        }
        zatvoriFormu();
        frmKorisnik.osvezi();
    }//GEN-LAST:event_btnPosaljiTestSamoproceneActionPerformed

