    private void srediFormu() {
        this.setTitle("Panel za testiranje");
        Test poslednjaSamoprocena = frmKorisnik.poslednjaSamoprocena();
        Date dps = poslednjaSamoprocena != null ? poslednjaSamoprocena.getDatum() : null;
        Date danas = new Date();
        boolean radjenDanas = dps != null && Kontroler.getInstance().formatDatum(danas).equals(Kontroler.getInstance().formatDatum(dps));        
        if (radjenDanas) {
            btnPocniTestSamoprocene.setVisible(false);
            pnlTestSamoprocene.setVisible(false);
            pnlBrziIPCR.setVisible(true);
        } else {
            pnlTestSamoprocene.setVisible(false);
            pnlBrziIPCR.setVisible(false);
        }

        inicijalizujNizCbs();

    }

    private void inicijalizujNizCbs() {
        cbs = new JComboBox[6];
        cbs[0] = cbKontakt;
        cbs[1] = cbTemperatura;
        cbs[2] = cbKasalj;
        cbs[3] = cbSlabost;
        cbs[4] = cbMiris;
        cbs[5] = cbUkus;

        for (JComboBox cb : cbs) {
            cb.removeAllItems();
            for (Odgovor odg : Odgovor.values()) {
                cb.addItem(odg.toString());
            }
            cb.setSelectedItem(Odgovor.izaberite.toString());
        }
    }

    private void zatvoriFormu() {
        this.setVisible(false);
        frmKorisnik.setVisible(true);
        this.dispose();
    }
